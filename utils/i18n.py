def get_lang() -> str:
    """获取当前语言，默认中文"""
    return st.session_state.get("lang", "zh")


def t(key: str, *args) -> str:
    """翻译函数，支持 format 参数"""
    lang = get_lang()
    text = TRANSLATIONS.get(lang, TRANSLATIONS["zh"]).get(key, key)
    if args:
        try:
            text = text.format(*args)
        except Exception:
            pass
    return text
